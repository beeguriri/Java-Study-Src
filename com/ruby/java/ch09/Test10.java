package com.ruby.java.ch09;

public class Test10 {

	public static void main(String[] args) {

		System.out.println(Math.E);				//public static final double E = 2.7182818284590452354;
		System.out.println(Math.PI);			//public static final double PI = 3.14159265358979323846;
		
		System.out.println(Math.abs(-12));		//절대값 12
		System.out.println(Math.ceil(12.5));	//올림 13.0
		System.out.println(Math.floor(12.5));	//버림 12.0	 
		System.out.println(Math.max(5, 8));		//최댓값 8
		System.out.println(Math.min(5, 8));	    //최솟값 5
		System.out.println(Math.pow(2, 3));     //제곱 2^3 = 8
		System.out.println(Math.random());      //0~1 사이의 난수 생성 0.xxxxxxx
		System.out.println(Math.round(12.5));	//반올림 13
		System.out.println(Math.sqrt(4));	    //제곱근 2.0

	}
}
